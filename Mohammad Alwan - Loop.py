# Prompt user to enter highest value to count to
max_value = int(input('Count to: '))

counter = 1  # Initialize counter to 1


# Count from 1 to the number the user entered

while counter < max_value + 1:
    print(counter)  # Display counter
    counter += 2  # Increment counter




